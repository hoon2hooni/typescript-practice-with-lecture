  class CheapMilkSteamer {
    private steamMilk(): void {
      console.log('Steaming some milk... ');
    }
    makeMilk(cup: CoffeeCup): CoffeCup {
      this.steamMilk();
      return {
        ...cup,
        hasmilk: true,
      };
    }
  }
